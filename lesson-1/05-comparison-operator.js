// Оператори порівняння

console.log(5 < 10);
console.log(5 > 10);

// Переоприділення типів до числа

console.log('15' > 12);

// Оператори рівності (==), (===) та нерівності (!=), (!==)

console.log('10' === 10)
console.log('10' !== 10)


// При не строгому порівнянні типи приводяться до рядка, бажано не використовувати!!!
console.log('10' == 10)
console.log('10' != 10)

console.log(false == 0)